package Project_Socket;

public class Operazioni {
    public static int add(int a, int b)
    {
        return a+b;
    }
    public static int sott(int a, int b)
    {
        return a-b;
    }
    public static int molt(int a, int b)
    {
        return a*b;
    }
    public static int div(int a, int b)
    {
        return a/b;
    }
}
